#include "3-calc.h"

/**
 * op_add  - function that add two numbers.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: returns sum.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtracts any two numbers.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: returns difference.
 */

int op_sub(int a, int b)
{
	return ( a - b);
}

/**
 * op_mul - function that multiplies two numbers
 * @a: the first number.
 * @b: the second number.
 *
 * Return: returns product.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two numbers.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: returns division.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that gives the remainder after division of two numbers.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: returns remainder of division.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
